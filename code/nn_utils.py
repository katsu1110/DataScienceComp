# Mish activation
class Mish(Layer):
    def __init__(self, **kwargs):
        super(Mish, self).__init__(**kwargs)

    def build(self, input_shape):
        super(Mish, self).build(input_shape)

    def call(self, x):
        return x * K.tanh(K.softplus(x))

    def compute_output_shape(self, input_shape):
        return input_shape

